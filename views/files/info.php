 
<p><strong>GIS </strong>is a customizable,collaborative,powerful cloud computing&nbsp;&nbsp;solution for School ERP system.</p>
<p>The <strong>GIS Software Package</strong> includes the following system modules:</p>
<ol>
<li>K-12 Grading System (Complete | Advanced)</li>
<li>Enrollment &amp; Registration System (Complete)</li>
<li>Accounting System (Partial)</li>
<li>Guidance &amp; Counseling System (Basic)</li>
<li>Student Attendance System (1) RFID &nbsp;(2) Manual Input</li>
<li>Canteen POS System (Basic)</li>
<li>Employee Records (Work in Progress)</li>
</ol>
<p>&nbsp;</p>
<p><strong>The System Objectives are</strong>:</p>
<ol>
<li>To manage grading information via an integrated database cloud computing system</li>
<li>To feature standardized measurement of students' achievements</li>
<li>To provide convenient reporting tools for schools</li>
</ol>
<ul>
<li>&nbsp; &nbsp;Report cards of students</li>
<li>&nbsp; &nbsp;Teachers' class records</li>
<li>&nbsp; &nbsp;Deped school form requirements</li>
</ul>
