<li>Model of Class Records</li>
<li>Type of Computing Algorithm</li>
<li>Type of Transmutation Algorithm</li>
<li>Class Ranks - All or With Honors only</li>
<li>Settings like floor grade, floor score, lockdown dates</li>
<li>GIS Months Quarters i.e. Q1 - Jun, Jul, Aug; Q2 - Sep, Oct</li>
<li>Sample Printout of Report Cards - all levels</li>
<li>School Calendar - Distribution of report cards schedule; Printing at least 3 days before, all class records must be finalized by then; Minimize re-opening records and changing of grades</li>
</ol>
