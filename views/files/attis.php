<p><span><strong>ATTIS (Attendance Information System) Guideline</strong></span></p>
<p><strong><span>(Smart Card ID using RFID Technology)</span></strong></p>
<p><span>&nbsp;</span></p>
<p><span>1.No borrowing of other person's ID.</span></p>
<p><span>2. No logging of attendance for someone else.</span></p>
<p><span>3. Report immediately to the School Admin upon losing the ID for replacement.</span></p>
<p><span>4. First In Last Out System - tap your ID upon entry and before leaving the school.</span></p>
<p><span>5. You may view your monthly attendance by logging into your GIS account &gt; attendance link.</span></p>
<p><span>&nbsp;&nbsp;</span></p>
<p><span>&nbsp;</span></p>
<p><strong><span>Benefits</span></strong></p>
<p><span>&nbsp;</span></p>
<p><span>1. Records and maintains accurate attendance information on everyone</span></p>
<p><span>2. Tracks late entries, early outs, presence in premise but not reporting where necessary</span></p>
<p><span>3. Daily, fortnightly, monthly, yearly absentee/attendance report</span></p>
<p><span>4. Automated hence No manual effort intensive and time-consuming labor; saves time</span></p>
<p><span>5. Environment friendly by reducing use of paper</span></p>
<p><span>6. Improves administrative productivity and lessen stress</span></p>
<p><span>7. Involvement of few manpower to process tedious tasks</span></p>
