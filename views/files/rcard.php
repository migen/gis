<h2>REPORT CARD Printing - Notes</h2>
<p>I. Three (3) Steps to Printing Report Cards</p>
<ol start="1">
<li>Summarizer (by Adviser / Registrar / MIS)</li>
<li>Class Ranks (by Adviser / Registrar / MIS)</li>
<li>Print Report Card (by Registrar / MIS)</li>
</ol>
<p>&nbsp;</p>
<p>II. Three (3) Types of Class Ranks</p>
<ol start="1">
<li>All - same integer for TIES</li>
<li>Split - decimal form, divide between / among tied general averages (may require to overwrite ranks manually)</li>
<li>Domino - only qualified with no grades below honor mark and not disqualified in previous quarter</li>
</ol>
<p>&nbsp;</p>
<p>III. Two (2) Representations of Final Average (Q - quarter / qtr)</p>
<ol start="1">
<li>Q5 - for final average of</li>
<ol start="1">
<li>regular / non-semestral</li>
<li>1st semester (semestral or senior HS - levels.is_sem = 1)</li>
</ol>
<li>Q6 - for 2nd semester final average (for senior HS or college)</li>
</ol>
<p>&nbsp;</p>
<p>IV. Steps By Adviser</p>
<ol start="1">
<li><span style="text-decoration: underline;">Encode</span> &amp; <span style="text-decoration: underline;">Finalize</span> all subject courses in a section / classroom</li>
<li>Tally Aggregates (bottoms up policy, i.e. MAPEH before Makabayan) &gt; <span style="text-decoration: underline;">Tally</span></li>
<li><span style="text-decoration: underline;">Update</span> &amp; then <span style="text-decoration: underline;">Finalize</span> - Attendance</li>
<li><span style="text-decoration: underline;">Update</span>, <span style="text-decoration: underline;">Finalize</span>, <span style="text-decoration: underline;">Sort Ranks </span>&nbsp;- Conduct</li>
<li>Home &gt; Advisories &gt; Submissions &gt; Summarizer &gt; Summarize (genave)</li>
<li>Home &gt; Advisories &gt; Ranks (choose from 3 types of class ranks) &gt; <span style="text-decoration: underline;">Sort</span> (overwrite ranks if necessary) &gt; <span style="text-decoration: underline;">Update</span></li>
<li>*After processing the 4th Quarter period, make sure to process the final class ranks (QCR) or Q5 (Q6 for senior HS)&nbsp;</li>
</ol>
<p>&nbsp;</p>
<p>V. Steps By Admin (Registrar / MIS)</p>
<ol start="1">
<li>Home &gt; CIR &gt; 1) Summarize, 2) Rank, 3) Card</li>
</ol>
