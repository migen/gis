<h4>
POLICIES RELATED TO PAYMENT OF SCHOOL FEES, OFFICIAL RECORDS AND ADMINISTRATION OF QUARTERLY EXAM

</h4>
<div>
<p>
1.	Transferee student/pupil should submit his/her Form 137 within the School Year of transfer, otherwise enrollment is deemed temporary and enrollment for the succeeding SY may be denied.

</p>
<p>
2.	Tuition and other fees may be paid in cash, or check. Check must be on date. Addresses must be St. James Academy.

</p>
<p>
3.	For check payment, the following information must be indicated at the back of the check:
</p>

<p>
➢	Name of Child

</p>
<p>
➢	Contact Person
➢	Contact Number

</p>
<p>
➢	Contact Person
➢	Contact Number

</p>
<p>
4.	Students who opt for the installment plan must pay the prescribe amount on the due date. Otherwise surcharge will be imposed.
</p>

<p>
5.	Tuition and other fees may be refunded as shown below.

</p>

<p>
When a student registers in school, it is understood that he/she is enrolling for the entire school year. A student who withdraws from the school will be charged:

</p>
<p>

</p>
<p>
➢	5% of the total amount due for the term if he withdraws before classes start.
</p>
<p>
➢	10% of the total amount due for the term if he withdraws within the first week of classes regardless of whether or not he/she has actually attended classes.
</p>
<p>➢	20% of the total amount due for the term if he withdraws within the second week of classes regardless of whether or not he/she has actually attended classes.
</p>
<p>➢	100% of the total amount due for the term if he withdraws any time after the second week of classes whether or not he/;she has actually attended classes.
</p>
<p>6.	Student who has unpaid accounts will not be given his/her report card and/or transcript of records nor be given letters of recommendation and/or certification of attendance.
</p>
<p>7.	Only cash or dated check payment will be accepted as settlement of accounts due on the 1st day of the scheduled examinations.
</p>
