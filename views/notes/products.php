<h4>
Notes on Products<br />
</h4>

<p>
1) RO Qty is reorder quantity, default quantity when making PO. <br />
2) RO Level is critical inventory or reorder level, alerts manager to make PO. <br />
3) Combo - comma separated product id's, no spaces. <br />
4) For suppliers not main - go to assignments. <br /> 
5) PO Cost is the next cost of the product when current inventory level runs out. <br /> 
6) Level at Current Cost - when falls zero or below will auto-update cost from PO Cost.<br /> 


</p>